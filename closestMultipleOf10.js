 // Given a number return the closest number to it that is divisible by 10.
 // Example :
	// 22 ===> 20
	// 25 ===> 30
	// 37 ===> 40
 function closestMultipleOf10(num) {
 	var dec = [10,20,30,40.50,60,70,80,90];
 	for (var i = 0; i < dec.length; i++) {
 		if(num <= dec[i]){
 	}return dec[i]
 		}
 }